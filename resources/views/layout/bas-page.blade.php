            <span class="titreindex1">1. Assurance décennale auto-entrepreneur</span>
            <p></p>
            <p class="contenuindex">
                Tout intervenant dans l’édification d’un ouvrage sous le statut d’auto-entrepreneur est dans
                l’obligation de souscrire une assurance décennale pour auto-entrepreneur (sauf si
                l’intervenant n’est qu’un sous-traitant). La spécificité du contrat proposé est que la
                couverture est généralement proposée pour un maximum de deux activités et un chiffre
                d’affaires annuel avoisinant les 50 000,00€.


            </p>



            <span class="titreindex1">2. Assurance décennale artisan</span>
            <p></p>
            <p class="contenuindex">
                Généralement entrepreneur individuel, un artisan exerçant une activité liée au secteur du
                bâtiment est immatriculé à la chambre des métiers et de l’artisanat et n’emploie pas plus de
                10 salariés.
                Un nombre maximum d’activité n’est pas appliqué aux assurances décennales souscrites par
                les artisans et ne comporte pas de plafonnement du chiffre d’affaires.


            </p>

            <span class="titreindex1">3. Assurance décennale Entreprise</span>
            <p></p>
            <p class="contenuindex">
                Généralement ce sont les sociétés de taille moyenne et grande exerçant une activité liée au
                secteur du bâtiment sous le statut de SARL ou SAS et qui souscrivent leur assurance
                décennale auprès de distributeurs proposant une assurance décennale pour les entreprises
                de petites et grande taille.


            </p>





