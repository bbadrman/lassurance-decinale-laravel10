            <span class="titreindex1">1. Définition</span>

            <p></p>
            <p class="contenuindex">
                L’assurance décennale est une garantie qui permet à chaque participant à l’édification d’un
                ouvrage, d’être couvert par une assurance permettant d’indemniser le maitre d’ouvrage
                contre les éventuels sinistres pouvant compromettre la solidité de l’ouvrage et le rendre
                impropre à sa destination et cela pendant une durée de 10 dix ans à compter de la date de
                livraison du bâtiment. Cela concerne à la fois l’édification d’un bâtiment ou/et sa
                rénovation.
            </p>
            <p></p>

            <span class="titreindex1">2. Obligation législative</span>
            <p></p>
            <p class="contenuindex">
                L’assurance décennale est une garantie obligatoire pour tout professionnels œuvrant dans
                le secteur du bâtiment. Cette obligation est instaurée en 1978 grâce à la loi Spinetta, qui
                stipule que tout intervenant direct ou indirect doit fournir au maitre d’ouvrage une
                attestation d’assurance décennale avant l’ouverture du chantier.
                L’assurance décennale est souscrite par des professionnels, donc justifier la forme juridique
                du souscripteur est obligatoire.


            </p>
            <p></p>
